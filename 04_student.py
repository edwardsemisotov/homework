# –ï–∂–µ–º–µ—Å—è—á–Ω–∞—è —Å—Ç–∏–ø–µ–Ω–¥–∏—è —Å—Ç—É–¥–µ–Ω—Ç–∞ —Å–æ—Å—Ç–∞–≤–ª—è–µ—Ç educational_grant —Ä—É–±., –∞ —Ä–∞—Å—Ö–æ–¥—ã –Ω–∞ –ø—Ä–æ–∂–∏–≤–∞–Ω–∏–µ –ø—Ä–µ–≤—ã—à–∞—é—Ç —Å—Ç–∏–ø–µ–Ω–¥–∏—é
# –∏ —Å–æ—Å—Ç–∞–≤–ª—è—é—Ç expenses —Ä—É–±. –≤ –º–µ—Å—è—Ü. –†–æ—Å—Ç —Ü–µ–Ω –µ–∂–µ–º–µ—Å—è—á–Ω–æ —É–≤–µ–ª–∏—á–∏–≤–∞–µ—Ç —Ä–∞—Å—Ö–æ–¥—ã –Ω–∞ 3%, –∫—Ä–æ–º–µ –ø–µ—Ä–≤–æ–≥–æ –º–µ—Å—è—Ü–∞
# –°–æ—Å—Ç–∞–≤—å—Ç–µ –ø—Ä–æ–≥—Ä–∞–º–º—É —Ä–∞—Å—á–µ—Ç–∞ —Å—É–º–º—ã –¥–µ–Ω–µ–≥, –∫–æ—Ç–æ—Ä—É—é –Ω–µ–æ–±—Ö–æ–¥–∏–º–æ –µ–¥–∏–Ω–æ–≤—Ä–µ–º–µ–Ω–Ω–æ –ø–æ–ø—Ä–æ—Å–∏—Ç—å —É —Ä–æ–¥–∏—Ç–µ–ª–µ–π,
# —á—Ç–æ–±—ã –º–æ–∂–Ω–æ –±—ã–ª–æ –ø—Ä–æ–∂–∏—Ç—å —É—á–µ–±–Ω—ã–π –≥–æ–¥ (10 –º–µ—Å—è—Ü–µ–≤), –∏—Å–ø–æ–ª—å–∑—É—è —Ç–æ–ª—å–∫–æ —ç—Ç–∏ –¥–µ–Ω—å–≥–∏ –∏ —Å—Ç–∏–ø–µ–Ω–¥–∏—é.
# –§–æ—Ä–º–∞—Ç –≤—ã–≤–æ–¥–∞:
#   –°—Ç—É–¥–µ–Ω—Ç—É –Ω–∞–¥–æ –ø–æ–ø—Ä–æ—Å–∏—Ç—å –•–•–•.–•–• —Ä—É–±–ª–µ–π

educational_grant, expenses = 10000, 12000

month = 0
educational_grant_sum = 0
expenses_sum = 0

exp = expenses

while month < 10:
    expenses_sum += exp
    exp += exp * 0.03
    educational_grant_sum += educational_grant
    month += 1
    # –µ—Å—Ç—å —Ç–∞–∫–æ–π –æ–ø–µ—Ä–∞—Ç–æ—Ä += —Å–ª–æ–∂–µ–Ω–∏–µ —Å –ø—Ä–∏—Å–≤–æ–µ–Ω–∏–µ–º –Ω–∞–∑—ã–≤–∞–µ—Ç—Å—è

give_me_money = expenses_sum - educational_grant_sum
print('–°—Ç—É–¥–µ–Ω—Ç—É –Ω–∞–¥–æ –ø–æ–ø—Ä–æ—Å–∏—Ç—å', round(give_me_money, 2), '—Ä—É–±–ª–µ–π')

